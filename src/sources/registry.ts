// Build lookup maps for fast resolution
const byId = new Map<string, LibraryEntry>(
  LIBRARY_REGISTRY.map((l) => [l.id, l]),
);

const byAlias = new Map<string, LibraryEntry>();
for (const entry of LIBRARY_REGISTRY) {
  for (const alias of [entry.name.toLowerCase(), ...entry.aliases]) {
    byAlias.set(alias.toLowerCase(), entry);
  }
  if (entry.npmPackage) {
    byAlias.set(entry.npmPackage.toLowerCase(), entry);
  }
  if (entry.pypiPackage) {
    byAlias.set(entry.pypiPackage.toLowerCase(), entry);
  }
}

export function lookupById(id: string): LibraryEntry | undefined {
  return byId.get(id);
}

export function lookupByAlias(name: string): LibraryEntry | undefined {
  return byAlias.get(name.toLowerCase());
}

export function fuzzySearch(query: string, limit = 5): LibraryEntry[] {
  const q = query.toLowerCase();
  const scored: Array<{ entry: LibraryEntry; score: number }> = [];

  for (const entry of LIBRARY_REGISTRY) {
    let score = 0;
    const nameL = entry.name.toLowerCase();

    if (nameL === q) score += 100;
    else if (nameL.startsWith(q)) score += 60;
    else if (nameL.includes(q)) score += 30;

    for (const alias of entry.aliases) {
      const a = alias.toLowerCase();
      if (a === q) score += 90;
      else if (a.startsWith(q)) score += 50;
      else if (a.includes(q)) score += 20;
    }

    if (entry.npmPackage?.toLowerCase().includes(q)) score += 15;
    if (entry.tags.some((t) => t.includes(q))) score += 10;

    if (score > 0) scored.push({ entry, score });
  }

  return scored
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map((s) => s.entry);
}
